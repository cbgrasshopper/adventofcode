const characterCounts = name => {
  let charCounts = [];
  for (let char of name.replaceAll('-','').split('')) {
    const charEntry = charCounts.find(entry => entry.char == char);
    if (charEntry) {
      charEntry.count += 1;
    } else {
      charCounts.push({'char':char, 'count':1});
    }
  }
  return charCounts.sort((first, second) => {
    if (first.count == second.count) {
      return first.char < second.char ? -1 : 1;
    }
    return first.count < second.count ? 1 : -1;
  });
}

